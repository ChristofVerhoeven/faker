  Returns a complete name (may include a suffix/prefix or both)

  ## Examples

      iex> Faker.Person.Nl.name()
      "Dhr. Alberta Theunis Jr."
      iex> Faker.Person.Nl.name()
      "Marina Van Kessel"
      iex> Faker.Person.Nl.name()
      "Derek Terlouw"
      iex> Faker.Person.Nl.name()
      "Karin Rovers"
