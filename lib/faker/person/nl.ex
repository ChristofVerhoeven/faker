defmodule Faker.Person.Nl do
  import Faker, only: [sampler: 2]

  @moduledoc """
  Functions for name data in English
  """

  @doc """
  Returns a complete name (may include a suffix/prefix or both)

  ## Examples

      iex> Faker.Person.Nl.name()
      "Dhr. Noah Theunis Jr."
      iex> Faker.Person.Nl.name()
      "Richard Van Kessel"
      iex> Faker.Person.Nl.name()
      "Sabine Terlouw"
      iex> Faker.Person.Nl.name()
      "Rossie Rovers"
  """
